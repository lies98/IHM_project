export const recettePerMonth = (month,year) =>{
  return productsData.filter(el => parseInt(el.date.split("/")[1]) === month && parseInt(el.date.split("/")[2]) === year).map(el  => parseInt(el.Montant)).reduce((a,b)=> a+b,0)
}
export const recettePerYear = (year) =>{
  return productsData.filter(el => parseInt(el.date.split("/")[2]) === year).map(el  => parseInt(el.Montant)).reduce((a,b)=> a+b,0)
}

export const commandePending = () =>{
  return ordersTracks.filter((el,index) => el.state &&  parseInt(productsData[index].status) < 100 ).length
}
export const commandeSucess= () =>{
  return ordersTracks.filter((el,index) => el.state &&  parseInt(productsData[index].status) === 100 ).length
}
export const commandeCanceled= () =>{
  return ordersTracks.filter(el => !el.state).length
}
export const commandebyFournisseur = () =>{
  return productsData.map(el => el.Fournisseur).reduce(function (acc, curr) {
    if (typeof acc[curr] == 'undefined') {
      acc[curr] = 1;
    } else {
      acc[curr] += 1;
    }
  
    return acc;
  }, {});
}
